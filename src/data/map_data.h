// ─── 전체 맵 배열 ────────────────────────────────────────────
static MapDef* ALL_MAPS[] = {
    &MAP_0, &MAP_1, &MAP_2, &MAP_3,
    &MAP_4, &MAP_5, &MAP_6, &MAP_7
};
static const int NUM_MAPS = 8;

inline MapDef* getMap(int id) {
    if (id >= 0 && id < NUM_MAPS) return ALL_MAPS[id];
    return nullptr;
}

inline bool tileWalkable(char t) {
    return t == ' ' || t == '.' || t == ',' || t == ';';
}

inline bool tileIsEncounter(char t) {
    return t == ';';
}

inline char getTile(const MapDef* m, int x, int y) {
    if (!m || x < 0 || x >= MAP_W || y < 0 || y >= MAP_H) return '#';
    if (!m->tiles[y] || x >= (int)__builtin_strlen(m->tiles[y])) return '#';
    return m->tiles[y][x];
}
