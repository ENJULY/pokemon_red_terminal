// ─── 타일 종류 ───────────────────────────────────────────────
// ' '  빈 땅 (이동 가능)
// '.'  길 (이동 가능)
// ','  잔디 (이동 가능, 인카운터 없음)
// ';'  긴 풀 (이동 가능, 야생 인카운터)
// '#'  벽/건물 외벽 (통과 불가)
// 'T'  나무 (통과 불가)
// '~'  물 (통과 불가)
// 'D'  문 (건물 진입)
// 's'  표지판 (상호작용)
// 'N'  NPC 위치
// 'G'  체육관 문
// 'M'  마트 문
// 'C'  포켓몬센터 문
// 'L'  연구소 문 (오박사)

static const int MAP_W = 22;
static const int MAP_H = 16;

// ─── NPC 정의 ────────────────────────────────────────────────
struct NpcDef {
    int x, y;
    const wchar_t* lines[4]; // 대화 (nullptr 종료)
};

// ─── 트레이너 정의 ───────────────────────────────────────────
struct TrainerDef {
    int x, y;
    int dir;        // 0=아래 1=위 2=왼 3=오른 (바라보는 방향)
    int sightRange;
    const wchar_t* name;
    const wchar_t* preBattleText;
    int partyIds[3];    // 종ID (0=없음)
    int partyLevels[3];
    bool defeated;
};

// ─── 워프 포인트 ─────────────────────────────────────────────
struct WarpDef {
    int srcX, srcY;     // 이 타일을 밟으면 이동
    int destMap;        // 목적지 맵 ID
    int destX, destY;   // 목적지 좌표
};

// ─── 야생 인카운터 ───────────────────────────────────────────
struct EncounterEntry {
    int speciesId;
    int minLevel, maxLevel;
    int weight; // 출현 가중치 (합계 = 100)
};

// ─── 맵 정의 ─────────────────────────────────────────────────
struct MapDef {
    int         id;
    const char* name;           // ASCII 이름
    const wchar_t* nameW;       // 한글 이름
    const char* tiles[MAP_H];   // 각 행 문자열 (길이 = MAP_W)
    NpcDef      npcs[6];
    int         numNpcs;
    TrainerDef  trainers[4];
    int         numTrainers;
    WarpDef     warps[8];
    int         numWarps;
    EncounterEntry encounters[6];
    int         numEncounters;
    int         northMap;  // 북쪽 연결 맵 (-1=없음)
    int         southMap;  // 남쪽 연결 맵
    int         northEntryX, northEntryY; // 이 맵으로 북쪽에서 진입 시 좌표
    int         southEntryX, southEntryY; // 이 맵으로 남쪽에서 진입 시 좌표
    const wchar_t* bgmFile;
};

